//função para aparecer area de cadastro
$(document).ready(function(){
    $("#cadastrar").click(function(){
        $("#areaCadastro").show();
        $("#areaLogin").hide();
        
    });
});

//função para aparecer area de cadastro
$(document).ready(function(){
    $("#logar").click(function(){
        $("#areaCadastro").hide();
        $("#areaLogin").show();
        
    });
});



